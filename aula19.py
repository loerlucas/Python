primeiro_valor = input('Digite um valor: ' )
segundo_valor = input('Digite outro valor: ')

if primeiro_valor >= segundo_valor:
    print(f'Primeiro valor="{primeiro_valor}" é maior ou igual '
          f'que o Segundo valor="{segundo_valor}" ')
else:
    print(f'Segundo valor="{segundo_valor}" é '
          f'maior ou igual que o Primeiro valor="{primeiro_valor}" ')