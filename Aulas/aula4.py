# Tipos int e float
# int -> Numero inteiro
# O tipo int representa qualquer numero
# positivo ou negativo. O int sem sinal eh coniderado
# positivo 

# print(11) # int
# print(-11) # int
# print(0)

# float -> numero com ponto flutuante
# O tipo float represnta qualquer numero
# positivo ou negativo com ponto flutuante
# float sem sinal eh considerado positivo
# print(1.1,10.11)
# print(0.0, -1.5)

# A função type mostra o tipo que o Python
# inferiu valor

print(type('Nome'))
print(type(0))
print(type(-1))
print(type(1.1))
print(type(-1.1))
print(type(0.0))